package day17_While_DoWhile;
//While loop is repeated if statement
public class WhileLoopIntro {
    public static void main(String[] args) {
        if(true){
            System.out.println("Wooden Spoon");// 1 time
        }


        System.out.println("--------------------");

        while (true) {
            System.out.println("Wooden Spoon");//there is no limit. Multiple time
        }

    }
}
